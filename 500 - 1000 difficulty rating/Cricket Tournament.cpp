#include <bits/stdc++.h>

using namespace std;

int main() {
    int t;
    cin >> t;
    for (int i = 0; i < t; i++) {
        int n;
        int m;
        cin >> n >> m;
        if (n - 1 >= m) {
            cout << "YES";
        } else {
            cout << "NO";
        }
        cout << endl;
    }

}
